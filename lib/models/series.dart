class Series {
  final String? title;
  final String? publisher;
  final int? episodeCount;
  final DateTime? releaseDate;
  final String? story;
  final List<int>? characterIdList;
  final List<int>? episodeIdList;
  final String? thumbnailUrl;
  final String? backgroundUrl;

  Series({
    this.title,
    this.publisher,
    this.episodeCount,
    this.releaseDate,
    this.story,
    this.characterIdList,
    this.episodeIdList,
    this.thumbnailUrl,
    this.backgroundUrl,
  });
}
