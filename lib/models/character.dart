class Character {
  final String? story;
  final String? name;
  final String? realName;
  final String? aliases;
  final String? publisher;
  final List<String>? creators;
  final int? gender;
  final DateTime? birthDate;
  final String? iconUrl;
  final String? backgroundUrl;

  Character({
    this.story,
    this.name,
    this.realName,
    this.aliases,
    this.publisher,
    this.creators,
    this.gender,
    this.birthDate,
    this.iconUrl,
    this.backgroundUrl,
  });
}
